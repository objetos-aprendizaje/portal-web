function heroiconOutline(icons) {
    let svg =
        "<svg xmlns='http://www.w3.org/2000/svg' fill='none' viewBox='0 0 24 24' stroke-width='1.5' stroke='currentColor'>";

    if (Array.isArray(icons)) {
        icons.forEach((d) => {
            svg += `<path fill-rule='evenodd' d='${d}' />`;
        });
    } else {
        svg += `<path stroke-linecap='round' stroke-linejoin='round' d='${icons}' />`;
    }

    svg += "</svg>";

    return svg;
}

export function heroicon(icon, type = "outline") {
    let d;
    let svg;
    if (type === "outline") {
        d = getHeroiconOutline(icon);
        svg = heroiconOutline(d);
    } else if (type === "solid") {
        d = getHeroiconSolid(icon);
        svg = heroiconSolid(d);
    } else {
        svg = heroiconSolid(false);
    }
    return svg;
}

