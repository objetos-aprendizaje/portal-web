        $svg = "<svg xmlns='http://www.w3.org/2000/svg' fill='none' viewBox='0 0 24 24' stroke-width='1.5' stroke='currentColor'>";
    }

    if (is_array($icons)) {
        foreach ($icons as $d) {
            $svg .= "<path fill-rule='evenodd' d='$d' clip-rule='evenodd' />";
        }
    } else {
        $svg .= "<path fill-rule='evenodd' d='$icons' />";
    }

    $svg .= "</svg>";

    return $svg;
}

