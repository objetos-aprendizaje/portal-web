function heroicon($icon, $type = "outline", $color = null)
{

    if ($type == "outline") {
        $d = get_heroicon_d_outline($icon);
        $heroicon = heroicon_outline($d, $color);
    } elseif ($type == "solid") {
        $d = get_heroicon_d_solid($icon);
        $heroicon = heroicon_solid($d, $color);
    } else {
        $heroicon = heroicon_solid(false);
    }

    return $heroicon;
}

function heroicon_outline($icons, $color)
{
    if (isset($color)) {
        $svg = "<svg xmlns='http://www.w3.org/2000/svg' fill='none' viewBox='0 0 24 24' stroke-width='1.5' stroke='" . $color . "'>";
    } else {
        $svg = "<svg xmlns='http://www.w3.org/2000/svg' fill='none' viewBox='0 0 24 24' stroke-width='1.5' stroke='currentColor'>";
    }

    if (is_array($icons)) {
        foreach ($icons as $d) {
            $svg .= "<path fill-rule='evenodd' d='$d' />";
        }
    } else {
        $svg .= "<path stroke-linecap='round' stroke-linejoin='round'  d='$icons' />";
    }

    $svg .= "</svg>";

    return $svg;
}

function heroicon_solid($icons, $color = null)
{

